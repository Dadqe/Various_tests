# –¢—Ä–∞–Ω—Å—Ñ–æ—Ä–º–∞—Ü–∏—è –º–∞—Ç—Ä–∏—Ü—ã, –µ—Å–ª–∏ –µ—Å—Ç—å –ø–æ–≤—Ç–æ—Ä—è—é—â–∏–µ—Å—è —Å–∏–º–≤–æ–ª—ã
def transform_array(A):
    A_prev = A
    A_prev_T = [list(row) for row in zip(*A_prev)]
    A_out = []
    
    for i_s, s in enumerate(A_prev): # s-stroka (string)
        stroka = []
        
        for i_c, n in enumerate(s):     # c-column (—Å—Ç–æ–ª–±–µ—Ü) –¢—É—Ç —è –ø–æ–ª—É—á–∞—é –∫–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –≤ —Å—Ç—Ä–æ–∫–µ –∏ –±—É–¥—É —Å—Ä–∞–≤–Ω–∏–≤–∞—Ç—å, –µ—Å—Ç—å –ª–∏ —Ç–∞–∫–æ–µ –∂–µ –≤ —Å—Ç—Ä–æ–∫–µ –∏ –≤ —Å—Ç–æ–ª–±—Ü–µ. n - —á–∏—Å–ª–æ –≤ –∫–∞–∂–¥–æ–π –∫–æ–ª–æ–Ω–∫–µ
            count = 0
            el_in_cls = Element(n)
            instring = check_in_string(s, n)
            incolumn = check_in_column(A_prev_T[i_c], n)
            
            if instring-1:
                count += instring
                el_in_cls.color = '#FFFF00'
            if incolumn-1:
                count += incolumn
                el_in_cls.color = '#00FF00'
            if instring-1 and incolumn-1:
                el_in_cls.color = '#00bfff'
            
            el_in_cls.number = n * count if count else n
            stroka.append(el_in_cls)    # –¢—É—Ç —è –¥–æ–±–∞–≤–ª—è—é —ç–ª–µ–º–µ–Ω—Ç –∫–ª–∞—Å—Å–∞.... –í—ã–≥–ª—è–¥–∏—Ç –æ–Ω –≤—Å—ë —Ä–∞–≤–Ω–æ —Ç–∞–∫ –∂–µ. –≠—Ç–æ –ø—Ä–æ—Å—Ç–æ –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ –± –ø—Ä–∏—Ç–∞—â–∏—Ç—å —Å —Å–æ–±–æ–π —Ü–≤–µ—Ç —è—á–µ–π–∫–∏, –≥–¥–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è —ç—Ç–æ —á–∏—Å–ª–æ –¥–æ –º–æ–º–µ–Ω—Ç–∞ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è –≤—ã—Ö–æ–¥–Ω—ã—Ö —Ç–∞–±–ª–∏—Ü
        A_out.append(stroka)    # –î–æ–±–∞–≤–ª—è—é —Ç—Ä–∞–Ω—Å—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –≤ –≤—ã—Ö–æ–¥–Ω–æ–π –º–∞—Å—Å–∏–≤
    
    return A_out
    

def check_in_string(string: list, num: int) -> int:
    '''–°—á–∏—Ç–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π –≤ —Å—Ç—Ä–æ–∫–µ. –ü–µ—Ä–µ–¥–∞—Ç—å –Ω–∞–¥–æ —Å—Ç—Ä–æ–∫—É –∏ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø—Ä–æ–≤–µ—Ä—è—é. –ú–æ–∂–µ—Ç –≤–µ—Ä–Ω—É—Ç—å 0, —Å—á–∏—Ç–∞–µ—Ç—Å—è –∫–∞–∫ False, –∑–Ω–∞—á–∏—Ç –Ω–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è'''
    return string.count(num)    # –û—Ç–Ω–∏–º–∞—é –µ–¥–∏–Ω–∏—Ü—É, –ø–æ—Ç–æ–º—É —á—Ç–æ —Ç–∞–º –≤—Å–µ–≥–¥–∞ –±—É–¥–µ—Ç 1, —Å–∞–º–æ –ø—Ä–æ–≤–µ—Ä—è–µ–º–æ–µ —á–∏—Å–ª–æ


def check_in_column(column: list, num: int) -> int:
    '''–°—á–∏—Ç–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π –≤ —Å—Ç–æ–ª–±—Ü–µ. –ü–µ—Ä–µ–¥–∞—Ç—å –Ω–∞–¥–æ —Å—Ç–æ–ª–±–µ—Ü –∏ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø—Ä–æ–≤–µ—Ä—è—é. –ú–æ–∂–µ—Ç –≤–µ—Ä–Ω—É—Ç—å 0, —Å—á–∏—Ç–∞–µ—Ç—Å—è –∫–∞–∫ False, –∑–Ω–∞—á–∏—Ç –Ω–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è'''
    return column.count(num)

